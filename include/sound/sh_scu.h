/************************************************************************

	inline function

************************************************************************/
static inline int scu_find_data(int val, struct scu_config *data, int size)
{
	int i;
	struct scu_config *data_p = data;

	for (i = 0; i < size; data_p++, i++) {
		if (val == data_p->label)
			return data_p->value;
	}

	return -1;
}

